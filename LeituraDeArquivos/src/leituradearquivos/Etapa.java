package leituradearquivos;

/**
 *
 * @author aline
 */
public class Etapa {
    
    private String turno;
    private String dia;

    public Etapa(String turno, String dia) {
        this.turno = turno;
        this.dia = dia;
    }

    public String getTurno() {
        return turno;
    }

    public void setTurno(String turno) {
        this.turno = turno;
    }

    public String getDia() {
        return dia;
    }

    public void setDia(String dia) {
        this.dia = dia;
    }
    
    
    
}
